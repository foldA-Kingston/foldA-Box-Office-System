    <h1>Accessibility</h1>
    <h4>The Isabel Bader Centre for the Performing Arts is an accessible building. Features include:</h4>
    <br>
    <h3><b>Exterior Access</b></h3>
    <h4>
        <ul type = "disc">
            <li>The Isabel is located close to the "Bader/Tett Centres” City of Kingston transit stop 
            (on the accessible Express bus route) and is near a crossing intersection on King Street West.</li>
            <li>An accessible concrete ramp provides a gentle sidewalk slope that provides access from King Street 
            West and reaches an accessible sidewalk crossing to the main entrance.</li>
            <li>A parking drop-off area is located at the front entry.</li>
            <li>Off King Street West, there are 5 accessible visitor parking spaces in the small upper lot across 
            from the entrance.  Two additional spaces are located in the west parking lot at the bottom of the 
            drive in front of the stage door.  Please contact our Box Office for advice on accessible parking at 613-533-2424.</li>
            <li>The main entry is accessible on grade, with door operator buttons at the entry and vestibule doors.</li>
        </ul>
    </h4>

    <h3><b>Interior Access</b></h3>
    <h4>
        <ul type = "disc">
            <li>The ticket counter near the entrance has a wheelchair accessible counter section.</li>
            <li>An accessible elevator is located near the main entrance and provides access to all levels of the building.</li>
            <li>The west entrance to the building (primarily used by faculty/staff) has an accessible ramp next to two accessible parking spaces.</li>
            <li>A service elevator is located near the west entrance to levels 1 and 2.</li>
            <li>On level 1 there are six individual dressing rooms that have wheelchair accessible washrooms with accessible showers.</li>
            <li>There is a large single-user (gender-neutral), accessible washroom on level 1 (there are also two additional single-user washrooms 
            on this floor) along with accessible male and female designated washrooms.</li>
            <li>Doors have been removed at washroom entrances to reduce barriers.</li>
            <li>Each male and female choral dressing rooms has an accessible washroom and separate accessible shower.</li>
            <li>The lobby bar and café kitchen also have wheelchair accessible counter service areas.</li>
            <li>Corridors and rooms have been designed to allow for the turning radius of various mobility devices.</li>
        </ul>
    </h4>

    <h3><b>Performance and Instructional Spaces</b></h3>
    <h4>
        <ul type = "disc">
            <li>The performance hall has wheelchair accessible seating on both the main level and the mezzanine level (a total of 14) in various 
            locations and there are also a number of bariatric chairs available, if requested.</li>
            <li>The film screening room has an accessible podium and two wheelchair accessible seats at the front of the room.</li>
            <li>There are accessible podiums in the seminar rooms.</li>
            <li>The performance hall projection room has a lift to make it barrier-free.</li>
            <li>The performance hall will have a light for an American Sign Language Interpreter on the stage.</li>
            <li>Some assistive listening devices should be available for use in building, such as in the performance hall and film screening room.</li>
        </ul>
    </h4>
